package easy.merge_two_sorted_lists_21;

/**
 * @author Aleksey Kurkov. Created on 13.01.20
 * @version 1.0
 * Definition for singly-linked list.
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode(int x) {
        val = x;
    }

    @Override
    public String toString() {
        return "ListNode{" +
                "val=" + val +
                ", next=" + next +
                '}';
    }
}
